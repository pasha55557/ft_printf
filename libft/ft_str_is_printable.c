#include "libft.h"

int		ft_str_is_printable(char *str)
{
	int i;

	i = 0;
	if (!str)
		return (0);
	while (str[i] != '\0')
	{
		if (ft_iscntrl(str[i]))
			return (0);
		i++;
	}
	return (1);
}
