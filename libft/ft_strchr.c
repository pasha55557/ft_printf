char	*ft_strchr(const char *str, int ch)
{
	char *st;
	char chr;

	st = ((char*)str);
	chr = ((char)ch);
	while ((*st != chr) && (*st != '\0'))
		st++;
	if ((*st == '\0') && (chr != '\0'))
		return (0);
	return (st);
}
